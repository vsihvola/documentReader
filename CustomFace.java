    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String jTF = jTextField1.getText();
        count1 = 0;
        count2 = 0;
        count3 = 0;
        count4 = 0;
        count5 = 0;
        Scanner sc;
        try {
            sc = new Scanner(new FileReader(jTF));
            while (sc.hasNext()) {
                String nextWord = sc.next();
                System.out.println(nextWord);

                if (nextWord.equals("1")) {
                    count1++;
                }
                if (nextWord.equals("2")) {
                    count2++;

                }
                if (nextWord.equals("3")) {
                    count3++;

                }
                if (nextWord.equals("4")) {
                    count4++;

                }
                if (nextWord.equals("5")) {
                    count5++;

                }

            }

            System.out.println("Succefull calls:" + count1);
            System.out.println("Rejected calls:" + count2);
            System.out.println("Canceled calls:" + count3);
            System.out.println("Failed calls:" + count4);
            System.out.println("Redirected calls:" + count5);

        } catch (FileNotFoundException ex) {
            Logger.getLogger(CustomFace.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTextField2.setText("" + count1);
        jTextField3.setText("" + count2);
        jTextField4.setText("" + count3);
        jTextField5.setText("" + count4);
        jTextField6.setText("" + count5);
    }//GEN-LAST:event_jButton2ActionPerformed


